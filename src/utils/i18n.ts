export function getFinnishWeekday(dateStr: string, index: number, lang: Lang): string {
  const date = new Date(dateStr);
  if (lang === "fi") {
    const days = [
      "Sunnuntai",
      "Maanantai",
      "Tiistai",
      "Keskiviikko",
      "Torstai",
      "Perjantai",
      "Lauantai",
    ];
    // For index 0/1 we can use dynamic "Tänään", "Huomenna"
    if (index === 0) return "Tänään";
    if (index === 1) return "Huomenna";
    return days[date.getDay()];
  } else {
    const days = [
      "Sunday",
      "Monday",
      "Tuesday",
      "Wednesday",
      "Thursday",
      "Friday",
      "Saturday",
    ];
    if (index === 0) return "Today";
    if (index === 1) return "Tomorrow";
    return days[date.getDay()];
  }
}
